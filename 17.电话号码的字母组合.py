# @lc code=start
class Solution:
    def letterCombinations(self, digits: str) -> List[str]:
        Table = [
            ['a', 'b', 'c'],
            ['d', 'e', 'f'],
            ['g', 'h', 'i'],
            ['j', 'k', 'l'],
            ['m', 'n', 'o'],
            ['p', 'q', 'r', 's'],
            ['t', 'u', 'v'],
            ['w', 'x', 'y', 'z'],
            ]
        s = ''
        for i in range(len(digits)):
            num = digits[i]
            arr = Table[num-1]
            for j in range(len(arr)):
                char = arr[j]
                s = s+char

        
# @lc code=end

